#include <unistd.h>

void	ft_print_comb2(void)
{
	int		nums[2];
	char	s[5];

	nums[0] = -1;
	s[2] = ' ';
	while (nums[0] < 99)
	{
		nums[1] = nums[0]++;
		while (nums[1] < 100)
		{
			if (nums[0] != nums[1] && nums[0] < nums[1])
			{
				s[0] = (nums[0] / 10) + '0';
				s[1] = (nums[0] % 10) + '0';
				s[3] = (nums[1] / 10) + '0';
				s[4] = (nums[1] % 10) + '0';
				write(1, s, 5);
				if ((nums[0] != 98) || (nums[1] != 99))
				{
					write(1, ", ", 2);
				}
			}
			nums[1]++;
		}
	}
}
