int	ft_add(int a, int b)
{
	return (a + b);
}

int	ft_subtract(int a, int b)
{
	return (a - b);
}

int	ft_division(int a, int b)
{
	return (a / b);
}

int	ft_multiplication(int a, int b)
{
	return (a * b);
}

int	ft_remainder(int a, int b)
{
	return (a % b);
}
